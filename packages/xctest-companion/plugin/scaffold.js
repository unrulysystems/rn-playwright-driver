function buildableReference({ targetName, targetUuid, buildableName, projectName }) {
  return `<BuildableReference
               BuildableIdentifier = "primary"
               BlueprintIdentifier = "${xmlEscape(targetUuid)}"
               BuildableName = "${xmlEscape(buildableName)}"
               BlueprintName = "${xmlEscape(targetName)}"
               ReferencedContainer = "container:${xmlEscape(projectName)}.xcodeproj">
            </BuildableReference>`
}

function trimQuotes(value) {
  return String(value || '').replace(/^"|"$/g, '')
}

function xmlEscape(value) {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
}

